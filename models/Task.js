const { Model, DataTypes } = require('sequelize');
const sequelize = require('../config/connection');

class Task extends Model {}

Task.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true,
    },
    description: {
      type: DataTypes.STRING,
    },
    date_created: {
      type: DataTypes.DATE,
      allowNull: false,
      defaultValue: DataTypes.NOW,
    },
    completed:{
        type: DataTypes.BOOLEAN,
        defaultValue: false,
    },
    in_progress:{
        type: DataTypes.BOOLEAN,
        defaultValue: false,
    },
    list_id: {
      type: DataTypes.INTEGER,
      allowNull: false,

      references: {
        model: 'list',
        key: 'id',
      },
    },
  },
  {
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: 'task',
  }
);

module.exports = Task;